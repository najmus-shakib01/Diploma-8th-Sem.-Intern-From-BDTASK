# ডেটা টাইপ হলো ভ্যারিয়েবল কোন ধরনের ডেটা ধারণ করছে সেটা বোঝায়। Python অটোমেটিক ভাবে ডেটা টাইপ নির্ধারণ করে।

a = 'Hello'
b = 10
c = 10.75
d = True
e = ['apple', 'banana', 'cherry']
f = ('apple', 'banana', 'cherry')
g = {'apple', 'banana', 'cherry'}
h = {'name' : 'shakib', 'age': 22}

print(a, " : ", type(a))
print(b, " : ", type(b))
print(c, " : ", type(c))
print(d, " : ", type(d))
print(e, " : ", type(e))
print(f, " : ", type(f))
print(g, " : ", type(g))
print(h, " : ", type(h))